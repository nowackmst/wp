  function createMenuCard(cardData, modalId) {
    const menuCard = document.createElement("div");
    menuCard.className = "menu-card";
  
    const image = document.createElement("img");
    image.src = cardData.image;
    image.alt = cardData.title;
    image.className = "menu-card-img";
    menuCard.appendChild(image);
  
    const title = document.createElement("h3");
    title.className = "menu-card-title";
    title.textContent = cardData.title;
    menuCard.appendChild(title);
  
    const joinButton = document.createElement("button");
    joinButton.className = "join-testnet";
    joinButton.textContent = "JOIN TESTNET";
    joinButton.setAttribute("data-modal", modalId);
    menuCard.appendChild(joinButton);
  
    return menuCard;
  }
  
  function createModal(modalId, modalData) {
    const modalTemplate = document.getElementById("modal-template");
    const modal = modalTemplate.cloneNode(true);
    modal.id = modalId;
  
    modal.querySelector(".modal-title").textContent = modalData.title;
    modal.querySelector(".ipsam").textContent = modalData.content;
  
    const infoLinks = modal.querySelector(".info");
    infoLinks.innerHTML = "";
  
    modalData.links.forEach((linkData) => {
      const link = document.createElement("a");
      link.href = linkData.url;
      link.target = "_blank";
      link.setAttribute("data-label", linkData.label);
  
      const icon = document.createElement("i");
      icon.className = linkData.icon;
      link.appendChild(icon);
  
      infoLinks.appendChild(link);
  
      const linkSeparator = document.createTextNode(" ");
      infoLinks.appendChild(linkSeparator);
    });
  
    modal.style.display = "block";
    return modal;
  }
  
  const menuCardsContainer = document.getElementById("menu-cards");
  
  menuCards.forEach((cardData, index) => {
    const modalId = `modal-${index}`;
    const menuCard = createMenuCard(cardData, modalId);
    menuCardsContainer.appendChild(menuCard);
  
    const joinButton = menuCard.querySelector(".join-testnet");
    joinButton.addEventListener("click", (event) => {
      event.stopPropagation();
      const modal = createModal(modalId, cardData);
      document.body.appendChild(modal);
      window.addEventListener("click", outsideModalClickHandler);
    });
  });
  
  function outsideModalClickHandler(event) {
    const modal = document.querySelector(".modal");
    const menuCardsSection = document.getElementById("menu-testnet");
    if (modal && !modal.contains(event.target) && !menuCardsSection.contains(event.target)) {
      modal.style.display = "none";
      window.removeEventListener("click", outsideModalClickHandler);
    }
  }
  
  document.addEventListener("click", (event) => {
    if (event.target.classList.contains("close")) {
      const modal = event.target.closest(".modal");
      modal.style.display = "none";
      window.removeEventListener("click", outsideModalClickHandler);
    }
  });